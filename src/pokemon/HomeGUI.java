    private void jButton16MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton16MouseClicked
        boolean e = selectedMonster.flee();
        if (e) {
            jLayeredPane9.setVisible(true);
            jLayeredPane17.setVisible(false);
            jLabel48.setVisible(false);
            jLabel54.setVisible(false);
            JOptionPane.showMessageDialog(this, "Escape success", "Success", JOptionPane.WARNING_MESSAGE);
            
        } else {
            JOptionPane.showMessageDialog(this, "Escape fail", "Failed", JOptionPane.WARNING_MESSAGE);
            enemyTurn();
        jTextArea1.setText("Enemy's turn");
        javax.swing.Timer timer2 = new javax.swing.Timer(2000, new java.awt.event.ActionListener() {
                @Override
                public void actionPerformed(java.awt.event.ActionEvent e) {
                    Random random = new Random();
                    int action = random.nextInt(4) + 1; 

            switch(action) {
                case 1:
                case 2:
                    
                    enemy.attack(selectedMonster);
                    jTextArea1.setText("Enemy performed a regular attack");
                    break;
                case 3:
                    // Enemy performs a special attack
                    enemy.specialAttack(selectedMonster);
                    jTextArea1.setText("Enemy performed a special attack");
                    break;
                case 4:
                    // Enemy performs an elemental attack
                    enemy.elementalAttack(selectedMonster);
                    jTextArea1.setText("Enemy performed an elemental attack");
                    break;
                }

                    updateHP();
                    if(selectedMonster.curHp==0){
                        lose();
                        return;
                    }
                    playerTurn();
                    
                }
            });
            timer2.setRepeats(false);
            timer2.start();
        }
    }//GEN-LAST:event_jButton16MouseClicked

